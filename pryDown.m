function res = pryDown(Image)
    %% 函数介绍 
     
    % 完成高斯金字塔构建中的下采样操作
    % 参数输入：输入的图像Image
    % 输出：得到Image的下一层图像
    % -----------------------------------------------------------------
    
    %% 实现
     
    % 下采样高斯滤波，参数输入1
    Img = Gauss(Image, 1);
    
    % 仅提取奇数行列
    res = Img(1:2:end,1:2:end);
    
end